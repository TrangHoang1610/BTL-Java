    public void showTable() {
        items = new HoaDonDAO().getHoaDOns();
        model.setRowCount(0);
        for (HoaDon item : items) {
            model.addRow(new Object[]{
                model.getRowCount() + 1, item.getMaHD(), item.getMaNV(), item.getMaKH(), item.getNgayLap(), f.format(item.getTongTien())
            });
        }
    }

    public void getChiTietHoaDOns() throws SQLException {

        Connection conn = null;
        Statement st = null;

        String url = "jdbc:sqlserver://localhost:1433;databaseName=QLShowroom";
        String user = "sa";
        String pass = "123";

        conn = (Connection) DriverManager.getConnection(url, user, pass);
        st = (Statement) conn.createStatement();
        String sql = "select SANPHAM.MASP,TENSP,HOADON.SOLUONG,SANPHAM.GIA from HOADON INNER JOIN SANPHAM ON HOADON.MASP=SANPHAM.MASP where MAHD='"+ txtMaHD.getText() + "'";
        ResultSet rs = st.executeQuery(sql);
        String[] ar = {"Mã sản phẩm", "tên sản phẩm", "Số lượng", "đơn giá"};
        DefaultTableModel model1 = new DefaultTableModel(ar, 0);
        while (rs.next()) {
            Vector vec = new Vector();
            vec.add(rs.getString("MASP"));
            vec.add(rs.getString("TENSP").trim());
            vec.add(rs.getInt("SOLUONG"));
            vec.add(rs.getDouble("GIA"));
            model1.addRow(vec);
        }
        tableCTHD.setModel(model1);

    }

