    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if ("".equals(txtMaHD.getText())) 
            JOptionPane.showMessageDialog(this, "không để trống mã hóa đơn");  
        else if ("".equals(txtSoLuong.getText()))
            JOptionPane.showMessageDialog(this, "không để trống số lượng sản phẩm");  
        else {
            boolean isSuccess = true;

            String maHD = txtMaHD.getText();
            String maNV = cbMaNV.getSelectedItem().toString();
            String maSP = cbMaSP.getSelectedItem().toString();
            String maKH = cbMAKH.getSelectedItem().toString();
            String ngayLap = ((JTextField) DateChoose.getDateEditor().getUiComponent()).getText();
            int soLuong = Integer.parseInt(txtSoLuong.getText());

            if (isSuccess) {
                HoaDon item = new HoaDon(maHD, maNV, maSP, maKH, ngayLap, soLuong);
                if (new HoaDonDAO().addItem(item)) {
                    this.showTable();
                    JOptionPane.showMessageDialog(this, "Thêm mới thành công!");
                    clearText();
                } else {
                    JOptionPane.showMessageDialog(this, "Hóa đơn đã tồn tại!");
                }
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

